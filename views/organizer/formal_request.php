                <input type="text" name="proponent_name" class="form-control"
                       value="<?= htmlspecialchars($_POST['proponent_name'] ?? ($proposal['proponent_name'] ?? ($_SESSION['user']['firstname'] . ' ' . $_SESSION['user']['lastname']))) ?>"
                       required>
            </div>
            <div class="col-md-6">
                <label class="form-label">Association / Organization Name</label>
                <input type="text" name="association_name" class="form-control"
                       value="<?= htmlspecialchars($_POST['association_name'] ?? ($proposal['association_name'] ?? '')) ?>"
                       placeholder="e.g. Barangay Farmers Association">
            </div>
        </div>
        <div class="row">
            <div class="col-md-6">
                <label class="form-label">Recipient Name <span class="text-danger">*</span></label>
                <input type="text" name="recipient_name" class="form-control"
                       value="<?= htmlspecialchars($_POST['recipient_name'] ?? ($proposal['recipient_name'] ?? '')) ?>"
                       placeholder="Name of recipient officer" required>
            </div>
            <div class="col-md-6">
                <label class="form-label">Recipient Position / Title <span class="text-danger">*</span></label>
                <input type="text" name="recipient_position" class="form-control"
                       value="<?= htmlspecialchars($_POST['recipient_position'] ?? ($proposal['recipient_position'] ?? '')) ?>"
                       placeholder="e.g. Municipal Agriculture Officer" required>
            </div>
        </div>
    </div>

    <!-- Seedling / Material Request -->
    <div class="ps-card mb-4">
        <h6 class="fw-bold text-ps-green mb-3">
            <i class="bi bi-tree me-2"></i>Seedling / Material Request
        </h6>
        <div class="row">
            <div class="col-md-6 mb-3">
                <label class="form-label">Seedling Variety / Name <span class="text-danger">*</span></label>
                <select name="seedling_variety" class="form-select" required>
                    <option value="" disabled <?= empty($_POST['seedling_variety']) ? 'selected' : '' ?>>Select seedling variety</option>
                    <?php
                    $plantingList = [
                        'Banana Lakatan','Avocado','Marang','Durian','Lanzones',
                        'Rambutan','Jackfruit','Corn','Coffee','Cacao','Vegetable Seeds'
                    ];
                    $matLookup = [];
                    foreach ($materials as $m) $matLookup[$m['material_name']] = $m;
                    $selectedVariety = $_POST['seedling_variety'] ?? $proposal['seedling_type'] ?? '';
                    foreach ($plantingList as $pName):
                        $mat = $matLookup[$pName] ?? null;
                        $qtyLabel = ($mat && $mat['quantity'] > 0)
                            ? ' (' . number_format($mat['quantity']) . ' ' . htmlspecialchars($mat['unit']) . ' available)'
                            : '';
                    ?>
                        <option value="<?= htmlspecialchars($pName) ?>"
                            <?= $selectedVariety === $pName ? 'selected' : '' ?>>
                            <?= htmlspecialchars($pName) ?><?= $qtyLabel ?>
                        </option>
                    <?php endforeach; ?>
                </select>
            </div>
            <div class="col-md-6 mb-3">
                <label class="form-label">Quantity Requested <span class="text-danger">*</span></label>
                <input type="number" name="quantity_requested" class="form-control"
                       min="1" placeholder="Number of seedlings/packs"
                       value="<?= htmlspecialchars($_POST['quantity_requested'] ?? $proposal['quantity_requested']) ?>"
                       required>
            </div>
        </div>
    </div>

    <!-- Activity Details -->
    <div class="ps-card mb-4">
        <h6 class="fw-bold text-ps-green mb-3">
