<?php
$pageTitle = 'Generate Formal Request';
include __DIR__ . '/../partials/layout_head.php';

$requestId = intval($_GET['id'] ?? 0);
if (!$requestId) {
    echo '<div class="alert alert-danger">Invalid request.</div>';
    include __DIR__ . '/../partials/layout_foot.php';
    exit;
}

$srModel  = new ServiceRequest($conn);
$proposal = $srModel->getById($requestId);

// Security: must belong to this organizer and must be barangay_approved
if (!$proposal || $proposal['user_id'] != $_SESSION['user']['id'] || $proposal['status'] !== 'barangay_approved') {
    echo '<div class="alert alert-danger">This proposal is not available for formal request generation.</div>';
    include __DIR__ . '/../partials/layout_foot.php';
    exit;
}

// Check if formal request already submitted
$chk = $conn->prepare("SELECT id FROM formal_requests WHERE request_id = ?");
$chk->execute([$requestId]);
$existing = $chk->fetch();
if ($existing) {
    echo '<div class="alert alert-warning"><i class="bi bi-info-circle me-2"></i>A formal request has already been submitted for this proposal. <a href="index.php?action=my_requests">Go back</a></div>';
    include __DIR__ . '/../partials/layout_foot.php';
    exit;
}

$pmModel   = new PlantingMaterial($conn);
$materials = $pmModel->getAll();

// Handle submission
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $required = ['proponent_name','recipient_name','recipient_position','seedling_variety','quantity_requested','activity_date','activity_location','purpose'];
    $missing  = false;
    foreach ($required as $f) {
        if (empty(trim($_POST[$f] ?? ''))) { $missing = true; break; }
    }

    if ($missing) {
        $error = 'Please fill in all required fields.';
    } else {
        $stmt = $conn->prepare("
            INSERT INTO formal_requests
                (request_id, user_id, proponent_name, association_name, recipient_name,
                 recipient_position, seedling_variety, quantity_requested,
                 activity_date, activity_time, activity_location, purpose)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        ");
        $result = $stmt->execute([
            $requestId,
            $_SESSION['user']['id'],
            trim($_POST['proponent_name']),
            trim($_POST['association_name'] ?? ''),
            trim($_POST['recipient_name']),
            trim($_POST['recipient_position']),
            trim($_POST['seedling_variety']),
            intval($_POST['quantity_requested']),
            $_POST['activity_date'],
            $_POST['activity_time'] ?? null,
            trim($_POST['activity_location']),
            trim($_POST['purpose']),
        ]);

        if ($result) {
            // Update service request status
            $srModel->updateStatus($requestId, 'formal_request_submitted', 'Formal request generated by organizer.');

            // Notify affairs workers
            $affairsWorkers = (new User($conn))->getAllByRole('community_affairs_worker');
            $notifModel     = new Notification($conn);
            $senderName     = htmlspecialchars($_SESSION['user']['firstname'] . ' ' . $_SESSION['user']['lastname']);
            foreach ($affairsWorkers as $aw) {
                $notifModel->create($aw['id'], 'New Formal Request',
                    "$senderName submitted a formal request for proposal {$proposal['request_number']}. Please verify stock availability.");
            }
            $success = true;
        } else {
            $error = 'Failed to submit formal request. Please try again.';
        }
    }
}
