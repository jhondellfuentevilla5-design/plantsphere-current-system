        </h6>
        <div class="row mb-3">
            <div class="col-md-4">
                <label class="form-label">Activity Date <span class="text-danger">*</span></label>
                <input type="date" name="activity_date" class="form-control"
                       value="<?= htmlspecialchars($_POST['activity_date'] ?? $proposal['target_date']) ?>"
                       required>
            </div>
            <div class="col-md-4">
                <label class="form-label">Activity Time</label>
                <input type="time" name="activity_time" class="form-control"
                       value="<?= htmlspecialchars($_POST['activity_time'] ?? ($proposal['activity_time'] ?? '08:00')) ?>">
            </div>
            <div class="col-md-4">
                <label class="form-label">Location / Site <span class="text-danger">*</span></label>
                <input type="text" name="activity_location" class="form-control"
                       value="<?= htmlspecialchars($_POST['activity_location'] ?? $proposal['target_location']) ?>"
                       placeholder="Barangay, Municipality" required>
            </div>
        </div>
    </div>

    <!-- Purpose -->
    <div class="ps-card mb-4">
        <h6 class="fw-bold text-ps-green mb-3">
            <i class="bi bi-chat-left-text me-2"></i>Purpose / Justification
        </h6>
        <textarea name="purpose" class="form-control" rows="4" required
                  placeholder="Describe the purpose of this formal request..."><?= htmlspecialchars($_POST['purpose'] ?? $proposal['purpose']) ?></textarea>
    </div>

    <div class="d-flex gap-2">
        <button type="submit" class="btn btn-ps-primary">
            <i class="bi bi-send me-2"></i>Submit Formal Request
        </button>
